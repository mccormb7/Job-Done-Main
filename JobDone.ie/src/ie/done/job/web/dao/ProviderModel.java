package ie.done.job.web.dao;

public class ProviderModel {
	//"experience", "title", "domain","qualifications", "location")
	private String providerTitle;
	private String providerDomain;
	private String providerExperience;
	private String providerQualifications;
	private String providerLocation;
	
	
	public String getProviderTitle() {
		return providerTitle;
	}
	public void setProviderTitle(String providerTitle) {
		this.providerTitle = providerTitle;
	}
	public String getProviderDomain() {
		return providerDomain;
	}
	public void setProviderDomain(String providerDomain) {
		this.providerDomain = providerDomain;
	}
	public String getProviderExperience() {
		return providerExperience;
	}
	public void setProviderExperience(String providerExperience) {
		this.providerExperience = providerExperience;
	}
	public String getProviderQualifications() {
		return providerQualifications;
	}
	public void setProviderQualifications(String providerQualifications) {
		this.providerQualifications = providerQualifications;
	}
	public String getProviderLocation() {
		return providerLocation;
	}
	public void setProviderLocation(String providerLocation) {
		this.providerLocation = providerLocation;
	}
	
	
	
	
	
	
	

}
